# messing with booleans
# booleans can be True or False
# Boolean is the return type of the mathematical operatiors 
# = < > <= >=
#
# flipped with the not keyword
# be careful using and or 
#
# Author Michal Gondek

is_alive = True
print (f'is alive = {is_alive}')

var = (2 <= 4)
print (f'var is {var}')

grade = 400
invalid  = (grade <= 0) and (grade >= 100)
print (f'invalid is {invalid}')